public class BitwiseOperators{
    public static void main(String[] args) {
        //Binary AND Operator
        System.out.println((5 & 6));

        //Binary OR Operator
        System.out.println((5 | 6));

        //Binary XOR Operator
        System.out.println((5 ^ 6));

        //Binary ones's Complement
        System.out.println((~5));

        //Binary Left shift
        System.out.println((5 << 2));

        //Binary Right shift
        System.out.println((6 >> 1));


    }
}